                    ส่วนที่ 2 จะเป็นการพูดในเรื่องที่ถูกำหนดให้โดยผู้คุมสอบ โดยผู้เข้าสอบจะต้องถึงเรื่องนั้นๆ
                    คนเดียวเป็นเวลาประมาณ
                    2 นาที โดยมีเวลาเตรียมตัวให้ประมาณ 1นาที เมื่อพูดจบแล้วผู้คุมสอบจะถามคำถามถึงเรื่องที่ได้พูดมา <br>
                    ส่วนที่ 3 จะเป็นการพูดคุยถามตอบ ต่อเนื่องมาจากหัวเรื่องในส่วนที่สอง
                    เพื่อให้ผู้เข้าสอบอธิบายหรือแสดงความเห็นใน
