                    <font color="orange">IELTS</font> ย่อมาจาก International English Language Testing System
                    เป็นการทดสอบภาษาอังกฤษระดับนานาชาติ สำหรับผู้ที่ต้องการไปทำงาน หรือศึกษาต่อในประเทศอังกฤษ,
                    ออสเตรเลีย, นิวซีแลนด์ แต่ในปัจจุบันสามารถใช้ที่ประเทศ แคนนาดา และสหรัฐอเมริกาได้ด้วย การสอบ IELTS
                    นั้นจะแบ่งออกเป็นสองประเภท คือ <br>
                    1. Academic ซึ่งเป็นการสอบภาษาอังกฤษทางด้านวิชาการ เพื่อนำผลการทดสอบไปในเชิงวิชาการ เช่น
                    สมัครเรียนใน
                    มหาวิทยาลัยในประเทศที่ใช้ภาษาอังกฤษเป็นภาษาหลักเป็นต้น <br>
                    2. General Training เป็นการสอบภาษาอังกฤษในเรื่องทั่วๆ ไป
                    ผลการสอบมักจะถูกนำไปใช้ในเรื่องการขอวีซ่าเพื่อย้ายถิ่น
                    ฐานไปยังประเทศที่ใช้ภาษาอังกฤษเป็นหลักอย่างอังกฤษ หรือ ออสเตรเลีย เป็นต้น <br>
                    <br>

                    <font color="orange">โดยการสอบ IELTS จะทดสอบในเรื่อง</font> <br>
                    1. Listening ซึ่งประกอบไปด้วยคำถามทั้งสิ้น 40 ข้อ และมีเวลาในการสอบทั้งหมด 30 นาที
                    โดยผู้สอบจะต้องฟังเนื้อหา
                    เกี่ยวบทสนทนาระหว่างคนสองคน หรือจะเป็นบทพูดของคนๆ เดียว
                    แล้วตอบคำถามซึ่งผู้เข้าสอบจะได้ฟังบทพูดเพียงแค่
                    ครั้งเดียวเท่านั้น <br>
                    2. Reading ในการสอบ Reading นั้นจะแบ่งออกเป็นสองประเภทตามประเภทการสอบที่ผู้เข้าสอบได้เลือกเอาไว้
                    นั่นคือ
                    Academic Reading และ General Training Reading สิ่งแตกต่างระหว่างทั้งสองแบบนั้นก็คือ
                    ตัวบทความที่จะต้องอ่าน
                    Academic Reading นั้นบทความที่ผู้เข้าสอบจะได้อ่านจะเป็นบทความเกี่ยวกับ
                    บทความทางด้านวิชาการที่น่าสนใจทั่วๆไป
                    ซึ่งอาจจะประกอบได้ด้วยกราฟ หรือรูปภาพและอาจจะมีคำศัพท์เฉพาะทาง ซึ่งจะมีความหมายกำกับเอาไว้ General
                    Training Reading นั้นเนื้อเรื่อง หรือบทความจะเป็นข้อเท็จจริงทั่วๆไป
                    ซึ่งเกี่ยวข้องกับชีวิตประจำวันในประเทศที่ภาษา
                    อังกฤษเป็นหลัก รวมไปถึงเรื่องที่เกี่ยวข้องกับการทำงาน เช่นการสมัครงาน เป็นต้น ในส่วน Reading
                    นั้นจะมีคำถาม
                    ทั้งหมด 40 ข้อ และใช้เวลาสอบทั้งสิ้น 60 นาที <br>
                    3. Writing การสอบ Writing จะแบ่งออกเป็น Academic Writing และ General Writing เช่นเดียวกับการสอบ
                    Reading
                    โดยทั้งสองประเภทผู้เข้าสอบจะต้องเขียนเรียงความทั้งสิ้น 2 ชิ้น โดยชิ้นที่หนึ่งต้องเขียนอย่างน้อย 150
                    คำ และ 250 คำ
                    สำหรับชิ้นที่สอง โดยมีเวลาเขียนทั้งหมด 60 นาที Academic Writing งานเขียนชิ้นแรกนั้นจะมี กราฟ หรือ
                    ตารางข้อมูล
                    โดยผู้เข้าสอบจะต้องเขียนสรุป, รายงาน หรือ อธิบาย ข้อมูลจากตารางหรือกราฟนั้นๆ
                    ส่วนอีกชิ้นจะเป็นการเขียนแสดง
                    ความคิดเห็น หรือโต้แย้งในเรื่องต่างๆ General Training Writing
                    งานเขียนแรกจะเป็นการเขียนที่เกี่ยวข้องกับสถานการณ์
                    ต่างๆ ที่ถูกกำหนดให้ โดยผู้เข้าสอบจะต้องเขียนเพื่อสอบถามข้อมูล หรือให้ข้อมูล
                    ขึ้นอยู่กับว่าคำสั่งนั้นให้ทำอะไร ส่วน
                    อีกงานเขียนผู้เข้าสอบจะต้องเขียนแสดงความคิดเห็น หรือข้อโต้แย้งในเรื่องต่างๆ เช่นเดียวกับ Academic
                    Writing แต่ทว่า
                    หัวข้อนั้นจะเป็นเรื่องที่ค่อนข้างจะเป็นส่วนตัว หรือเกี่ยวข้องความเห็นตัวเองมากกว่าข้อเท็จจริง
                    มากกว่าหัวข้อของ
                    Academic Writing <br>
                    4. Speaking การสอบพูดนั้นจะเป็นการลักษณะการสัมภาษณ์กับผู้คุมสอบ โดยจะเวลาในส่วนนี้ประมาณ 10 - 15
                    นาที ต่อ
