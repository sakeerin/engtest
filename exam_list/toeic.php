                    หรือคำศัพท์เฉพาะทางในแต่ละ สาขาอาชีพ ซึ่งคะแนนผลการสอบ TOEIC นั้นจะถูกนำมาใช้โดยหลายๆ องค์กร หรือ
                    บริษัท เป็นตัวชี้วัดความสามารถทาง ด้านภาษาอังกฤษของแต่ละบุคคล เพื่อใช้ในการคัดสรรบุคคลเข้าทำงาน,
